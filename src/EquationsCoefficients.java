public class EquationsCoefficients {
    private final double a;
    private final double b;
    private final double c;
    private final double d;

    public EquationsCoefficients(double a, double b, double c, double d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

    public double getD() {
        return d;
    }
}
